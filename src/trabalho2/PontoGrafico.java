public class PontoGrafico {

    private int inX;
    private int inY;
    private int outX;
    private int outY;

    public PontoGrafico(int inX, int inY, int outX, int outY) {
        this.inX = inX;
        this.inY = inY;
        this.outX = outX;
        this.outY = outY;
    }

    public int getInX() {
        return inX;
    }

    public int getInY() {
        return inY;
    }

    public int getOutX() {
        return outX;
    }

    public int getOutY() {
        return outY;
    }

}
